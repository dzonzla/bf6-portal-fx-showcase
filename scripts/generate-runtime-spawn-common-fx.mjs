/* global console, process */
import fs from 'node:fs';
import path from 'node:path';

function fail(message) {
    console.error(message);
    process.exitCode = 1;
    return;
}

function extractEnumMembers(source, enumName) {
    const enumDecl = `export enum ${enumName}`;
    const start = source.indexOf(enumDecl);
    if (start < 0) return null;

    const braceStart = source.indexOf('{', start);
    if (braceStart < 0) return null;

    let i = braceStart + 1;
    let depth = 1;
    while (i < source.length && depth > 0) {
        const ch = source[i];
        if (ch === '{') depth++;
        else if (ch === '}') depth--;
        i++;
    }

    if (depth !== 0) return null;

    const body = source.slice(braceStart + 1, i - 1);

    // Remove line comments and block comments.
    const withoutComments = body
        .replace(/\/\*[\s\S]*?\*\//g, '')
        .replace(/\/\/.*$/gm, '');

    return withoutComments
        .split(',')
        .map((s) => s.trim())
        .filter(Boolean)
        .filter((s) => /^[A-Za-z_][A-Za-z0-9_]*$/.test(s));
}

function makeTsArray(enumMembers, prefix) {
    return enumMembers.map((name) => `    ${prefix}.${name},`).join('\n');
}

function makeStringArray(enumMembers) {
    return enumMembers.map((name) => `    '${name}',`).join('\n');
}

const projectRoot = process.cwd();
const modTypesPath = path.join(projectRoot, 'node_modules', 'bf6-portal-mod-types', 'index.d.ts');

if (!fs.existsSync(modTypesPath)) {
    fail(`Could not find bf6-portal-mod-types at: ${modTypesPath}`);
    process.exit(1);
}

const source = fs.readFileSync(modTypesPath, 'utf8');

const members = extractEnumMembers(source, 'RuntimeSpawn_Common');

if (!members) {
    fail('Failed to extract RuntimeSpawn_Common enum members from bf6-portal-mod-types/index.d.ts');
    process.exit(1);
}

const sfxNames = members.filter((name) => name.startsWith('SFX_'));

const vfxNames = members.filter((name) => {
    if (name.startsWith('SFX_')) return false;
    return name.startsWith('FX_') || name.startsWith('VFX_') || name.endsWith('_VFX');
});

const outPath = path.join(projectRoot, 'src', 'fx-showcase', 'generated', 'runtime-spawn-common-fx.ts');
fs.mkdirSync(path.dirname(outPath), { recursive: true });

const content = `// This file is generated by scripts/generate-runtime-spawn-common-fx.mjs
// Do not edit by hand.

export const RuntimeSpawnCommonVfxAssets: mod.RuntimeSpawn_Common[] = [
${makeTsArray(vfxNames, 'mod.RuntimeSpawn_Common')}
];

export const RuntimeSpawnCommonVfxNames: string[] = [
${makeStringArray(vfxNames)}
];

export const RuntimeSpawnCommonSfxAssets: mod.RuntimeSpawn_Common[] = [
${makeTsArray(sfxNames, 'mod.RuntimeSpawn_Common')}
];

export const RuntimeSpawnCommonSfxNames: string[] = [
${makeStringArray(sfxNames)}
];

export const RuntimeSpawnCommonFxStats = {
    vfxCount: ${vfxNames.length},
    sfxCount: ${sfxNames.length},
} as const;
`;

fs.writeFileSync(outPath, content, 'utf8');

console.log(
    `[generate-runtime-spawn-common-fx] Wrote ${outPath} (VFX: ${vfxNames.length}, SFX: ${sfxNames.length})`
);
